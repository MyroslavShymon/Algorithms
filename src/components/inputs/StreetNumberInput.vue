<template>
  <validation-provider
    v-slot="{ errors, valid }"
    name="Street number"
    rules="required|numeric|min_value:1"
  >
    <md-field
      :state="valid"
      :class="{
        'md-invalid': readClasses && !valid,
      }"
    >
      <label for="streetNumber">Street number:</label>
      <md-input
        name="streetNumber"
        id="streetNumber"
        autocomplete="streetNumber"
        v-model="streetNumber"
        required
        placeholder="Enter street number"
      />
      <!-- :disabled="sending" -->
      <span style="display: none">{{ streetNumberNameOut }}</span>
    </md-field>
    <div :state="valid">
      <span class="error" v-for="(error, index) in errors" :key="index">
        {{ error }}
      </span>
    </div>
  </validation-provider>
</template>

<script lang="ts">
import { Component, Vue } from "vue-property-decorator";
import { ValidationProvider } from "vee-validate";
import { MyStore } from "@/store/store/store";
import { useStore } from "vuex-simple";

@Component({
  components: {
    ValidationProvider,
  },
})
export default class StreetNumberInput extends Vue {
  public streetNumber = 1;
  public validStreetNumber = false;
  private declarated!: boolean;
  public store: MyStore = useStore(this.$store);

  private get readClasses(): boolean {
    if (this.declarated === undefined && this.streetNumber === 0) {
      this.declarated = true;
      return false;
    } else {
      this.declarated === true;
      return true;
    }
  }

  public get streetNumberNameOut(): number {
    this.store.persons.streetNumber = this.streetNumber;
    this.store.persons.validStreetNumber = this.validStreetNumber;
    return this.store.persons.streetNumber;
  }
}
</script>

<style scoped>
.md-field {
  margin: 4px 0 4px;
}
.error {
  color: #ff5252;
}
</style>
